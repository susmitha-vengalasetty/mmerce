// âœ… Create a clean map of image names (safe for weird filenames like "Dress (2).jpg")
export const imageMap = Object.fromEntries(
  Object.entries(images).map(([path, url]) => {
    let fileName = path.split('/').pop().split('.')[0];
    fileName = fileName
      .replace(/\s+/g, '')     // remove spaces
      .replace(/[()]/g, '')    // remove parentheses
      .replace(/[^a-zA-Z0-9]/g, '') // remove other unwanted chars
      .toLowerCase();
    return [fileName, url];
  })
);

